package com.support.movement;

public class CarMaintanceDTO {
	private String car_maintance_info_no;
	private String car_number;
	private String car_maintance_date;
	private String car_maintance_code;
	private String car_maintance_content;
	private String car_distance;
	
	public String getCar_maintance_info_no() {
		return car_maintance_info_no;
	}
	public void setCar_maintance_info_no(String car_maintance_info_no) {
		this.car_maintance_info_no = car_maintance_info_no;
	}
	public String getCar_number() {
		return car_number;
	}
	public void setCar_number(String car_number) {
		this.car_number = car_number;
	}
	public String getCar_maintance_date() {
		return car_maintance_date;
	}
	public void setCar_maintance_date(String car_maintance_date) {
		this.car_maintance_date = car_maintance_date;
	}
	public String getCar_maintance_code() {
		return car_maintance_code;
	}
	public void setCar_maintance_code(String car_maintance_code) {
		this.car_maintance_code = car_maintance_code;
	}
	public String getCar_maintance_content() {
		return car_maintance_content;
	}
	public void setCar_maintance_content(String car_maintance_content) {
		this.car_maintance_content = car_maintance_content;
	}
	public String getCar_distance() {
		return car_distance;
	}
	public void setCar_distance(String car_distance) {
		this.car_distance = car_distance;
	}
	
	
}
