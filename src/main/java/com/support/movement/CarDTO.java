package com.support.movement;

public class CarDTO {
	String car_info_no;
	String car_number;
	String car_year;
	String car_distance;
	String car_code;
	String car_driver;
	String driver_no;
	
	
	public String getDriver_no() {
		return driver_no;
	}
	public void setDriver_no(String driver_no) {
		this.driver_no = driver_no;
	}
	public String getCar_info_no() {
		return car_info_no;
	}
	public void setCar_info_no(String car_info_no) {
		this.car_info_no = car_info_no;
	}
	public String getCar_number() {
		return car_number;
	}
	public void setCar_number(String car_number) {
		this.car_number = car_number;
	}
	public String getCar_year() {
		return car_year;
	}
	public void setCar_year(String car_year) {
		this.car_year = car_year;
	}
	public String getCar_distance() {
		return car_distance;
	}
	public void setCar_distance(String car_distance) {
		this.car_distance = car_distance;
	}
	public String getCar_code() {
		return car_code;
	}
	public void setCar_code(String car_code) {
		this.car_code = car_code;
	}
	public String getCar_driver() {
		return car_driver;
	}
	public void setCar_driver(String car_driver) {
		this.car_driver = car_driver;
	}
	
	
}
